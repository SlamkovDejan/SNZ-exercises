from materials.decision_tree_learning import build_tree, classify, max_classification

if __name__ == '__main__':
    x = input() .split(', ')
    test_case = list(map(float, x[:-1])) + [int(x[-1])]

    thirty = int(len(dataset) * 0.3)

    first_data = dataset[:thirty]
    second_data = dataset[thirty:thirty*2]
    third_data = dataset[thirty*2:]

    first_tree = build_tree(first_data)
    second_tree = build_tree(second_data)
    third_tree = build_tree(third_data)

    # what we want (dict):
    # 0 -> 1; 1 -> 0; 2 -> 0
    # 0 -> 1; 1 -> 1; 2 -> 0
    # 0 -> 1; 1 -> 2; 2 -> 0
    # prediction: 1 (with most votes)

    # setting default values
    # votes[0] = 0; votes[1] = 0; votes[2] = 0
    votes = {0: 0, 1: 0, 2: 0}

    # casting votes (the predictions)
    first_pred = max_classification(classify(test_case, first_tree))
    second_pred = max_classification(classify(test_case, second_tree))
    third_pred = max_classification(classify(test_case, third_tree))

    # Ex: first_pred = 0; second_pred = 1; third_pred = 1
    votes[first_pred] = votes[first_pred] + 1 # first_pred = 0 -> votes[0] = votes[0] + 1 -> votes[0] = 0 + 1 -> votes[0] = 1
    votes[second_pred] = votes[second_pred] + 1 # second_pred = 1 -> votes[1] = votes[1] + 1 -> votes[1] = 0 + 1 -> votes[1] = 1
    votes[third_pred] = votes[third_pred] + 1 # third_pred = 1 -> votes[1] = votes[1] + 1 -> votes[1] =  1 + 1 -> votes[1] = 2

    # after voting : votes = {0: 1, 1: 2, 2: 0}
    # votes.items() = [(0, 1), (1, 2), (2, 0)] -> tuple[1] = value; tuple[0] = key

    # sorted(list/set, reverse=boolean, key=lambda) -> returns sorted list/set
    # key -> a lambda function that shows Python how to sort elements that don't have natural ordering
    # x (in lambda) -> represents one element in the list/set from the first argument
    sorted_list = sorted(votes.items(), reverse=True, key=lambda x: x[1])

    print(f"Glasovi: {votes}")

    prediction = sorted_list[0][0]
    if sorted_list[0][1] == sorted_list[1][1]:
        prediction = 'unknown'

    print(f"Predvidena klasa: {prediction}")


